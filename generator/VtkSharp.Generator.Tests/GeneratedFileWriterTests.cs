using VtkSharp.Generator.Core.Generation;

namespace VtkSharp.Generator.Tests;

public sealed class GeneratedFileWriterTests
{
    [Fact]
    public void WriteIfChanged_CreatesMissingFile()
    {
        var path = Path.Combine(CreateDirectory(), "generated", "vtkFoo_export_gen.cpp");

        var changed = GeneratedFileWriter.WriteIfChanged(path, "// <auto-generated/>\n");

        Assert.True(changed);
        Assert.Equal("// <auto-generated/>\n", File.ReadAllText(path));
    }

    [Fact]
    public void WriteIfChanged_DoesNotTouchIdenticalFile()
    {
        var path = Path.Combine(CreateDirectory(), "vtkFoo_export_gen.cpp");
        File.WriteAllText(path, "// <auto-generated/>\n");
        var timestamp = new DateTime(2026, 1, 1, 0, 0, 0, DateTimeKind.Utc);
        File.SetLastWriteTimeUtc(path, timestamp);

        var changed = GeneratedFileWriter.WriteIfChanged(path, "// <auto-generated/>\n");

        Assert.False(changed);
        Assert.Equal(timestamp, File.GetLastWriteTimeUtc(path));
    }

    [Fact]
    public void WriteIfChanged_DoesNotTouchFileWithOnlyLineEndingDifferences()
    {
        var path = Path.Combine(CreateDirectory(), "CMakePresets.json");
        File.WriteAllText(path, "{\r\n  \"version\": 6\r\n}\r\n");
        var timestamp = new DateTime(2026, 1, 1, 0, 0, 0, DateTimeKind.Utc);
        File.SetLastWriteTimeUtc(path, timestamp);

        var changed = GeneratedFileWriter.WriteIfChanged(path, "{\n  \"version\": 6\n}\n");

        Assert.False(changed);
        Assert.Equal(timestamp, File.GetLastWriteTimeUtc(path));
    }

    [Fact]
    public void WriteIfChanged_RewritesDifferentFile()
    {
        var path = Path.Combine(CreateDirectory(), "vtkFoo_export_gen.cpp");
        File.WriteAllText(path, "old\n");

        var changed = GeneratedFileWriter.WriteIfChanged(path, "new\n");

        Assert.True(changed);
        Assert.Equal("new\n", File.ReadAllText(path));
    }

    private static string CreateDirectory()
    {
        var directory = Path.Combine(Path.GetTempPath(), "VtkSharp.Generator.Tests", Guid.NewGuid().ToString("N"));
        Directory.CreateDirectory(directory);
        return directory;
    }
}
