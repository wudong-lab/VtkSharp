using System.Text;
using VtkSharp.Generator.Core.Types;
using VtkSharp.Generator.Core.Whitelist;

namespace VtkSharp.Generator.Core.Generation;

public sealed class CppExportEmitter
{
    private readonly ExportNameGenerator _exportNameGenerator = new();

    public string Emit(string className, IReadOnlyList<string> includeClassNames, bool hasStaticNew, IReadOnlyList<WhitelistFunction> functions)
    {
        var sb = new StringBuilder();
        var exportNames = this.CreateExportNames(className, functions);
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#include \"vtksharp_api.h\"");
        foreach (var include in includeClassNames.Distinct(StringComparer.Ordinal).Order(StringComparer.Ordinal))
            sb.AppendLine($"#include <{include}.h>");
        sb.AppendLine($"#include <{className}.h>");
        sb.AppendLine();
        if (hasStaticNew)
            sb.AppendLine($"VTKSHARP_API {className}* {className}_New() {{ return {className}::New(); }}");
        foreach (var function in functions)
        {
            if (hasStaticNew || function != functions[0])
                sb.AppendLine();
            EmitFunction(sb, className, function, exportNames[function]);
        }
        return sb.ToString();
    }

    private static void EmitFunction(StringBuilder sb, string className, WhitelistFunction function, string exportName)
    {
        var returnType = ToCppType(function.Return.Type);
        var parameters = new[] { $"{className}* self" }
            .Concat(function.Parameters.Select(parameter => $"{ToCppType(parameter.Type)} {parameter.Name}"));

        var arguments = string.Join(", ", function.Parameters.Select(parameter => parameter.Name));
        var call = $"self->{function.Name}({arguments})";
        if (function.Return.Type == "void")
            sb.AppendLine($"VTKSHARP_API {returnType} {exportName}({string.Join(", ", parameters)}) {{ {call}; }}");
        else
            sb.AppendLine($"VTKSHARP_API {returnType} {exportName}({string.Join(", ", parameters)}) {{ return {call}; }}");
    }

    private static string ToCppType(string type)
        => type switch
        {
            "void" => "void",
            "char" => "char",
            "int" => "int",
            "unsigned int" => "unsigned int",
            "long long" => "long long",
            "unsigned long long" => "unsigned long long",
            "double" => "double",
            "float" => "float",
            "bool" => "bool",
            "vtkTypeBool" => "vtkTypeBool",
            "vtkIdType" => "vtkIdType",
            "const char*" => "const char*",
            "char*" => "char*",
            "void*" => "void*",
            _ when IsFixedArray(type) => ToCppArrayPointerType(type),
            _ when IsVtkPointer(type) => type,
            _ => throw new NotSupportedException($"C++ type '{type}' is not supported by the MVP emitter."),
        };

    private static bool IsVtkPointer(string type)
    {
        var normalized = type.Replace("const", "", StringComparison.Ordinal).Trim();
        return normalized.StartsWith("vtk", StringComparison.Ordinal) && normalized.EndsWith("*", StringComparison.Ordinal);
    }

    private Dictionary<WhitelistFunction, string> CreateExportNames(string className, IReadOnlyList<WhitelistFunction> functions)
    {
        var overloadCounts = functions
            .GroupBy(function => function.Name, StringComparer.Ordinal)
            .ToDictionary(group => group.Key, group => group.Count(), StringComparer.Ordinal);

        return functions.ToDictionary(
            function => function,
            function => this._exportNameGenerator.Create(
                className,
                function.Name,
                function.Parameters.Select(parameter => new CanonicalType(parameter.Type)).ToList(),
                overloadCounts[function.Name] > 1));
    }

    private static bool IsFixedArray(string type)
        => type.EndsWith(']') && type.Contains('[', StringComparison.Ordinal);

    private static string ToCppArrayPointerType(string type)
    {
        var bracketIndex = type.IndexOf('[', StringComparison.Ordinal);
        var isConst = type.StartsWith("const ", StringComparison.Ordinal);
        var elementType = type[..bracketIndex].Replace("const ", "", StringComparison.Ordinal).Trim();
        return isConst ? $"const {elementType}*" : $"{elementType}*";
    }
}
