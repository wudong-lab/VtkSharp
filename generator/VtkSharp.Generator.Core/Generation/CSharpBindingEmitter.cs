using System.Text;
using VtkSharp.Generator.Core.Types;
using VtkSharp.Generator.Core.Whitelist;

namespace VtkSharp.Generator.Core.Generation;

public sealed class CSharpBindingEmitter
{
    private readonly ExportNameGenerator _exportNameGenerator = new();

    public string Emit(string namespaceName, string className, string baseClassName, bool hasStaticNew, IReadOnlyList<WhitelistFunction> functions)
    {
        var sb = new StringBuilder();
        var exportNames = this.CreateExportNames(className, functions);
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Diagnostics;");
        sb.AppendLine("using System.Runtime.InteropServices;");
        sb.AppendLine();
        sb.AppendLine($"namespace {namespaceName};");
        sb.AppendLine();
        sb.AppendLine($"public unsafe partial class {className} : {baseClassName}");
        sb.AppendLine("{");
        sb.AppendLine($"    protected {className}(nint nativePointer, bool ownsReference) : base(nativePointer, ownsReference) {{ }}");
        if (hasStaticNew)
        {
            sb.AppendLine($"    public new static {className} New() => new({className}_New(), ownsReference: true);");
        }

        sb.AppendLine($"    public new static {className} WeakReference(nint nativePointer) => new(nativePointer, ownsReference: false);");
        sb.AppendLine();
        sb.AppendLine($"    public new static {className} Register({className} sourceObject)");
        sb.AppendLine("    {");
        sb.AppendLine($"        var target = new {className}(sourceObject.NativePointer, true);");
        sb.AppendLine("        target.Register();");
        sb.AppendLine("        return target;");
        sb.AppendLine("    }");
        sb.AppendLine();

        foreach (var function in functions)
        {
            this.EmitPublicMethod(sb, className, function, exportNames[function]);
            sb.AppendLine();
        }

        sb.AppendLine("    #region Interop");
        if (hasStaticNew)
        {
            sb.AppendLine("    [DllImport(InteropInfo.NativeLibraryName)]");
            sb.AppendLine($"    private static extern nint {className}_New();");
        }
        foreach (var function in functions)
        {
            if (hasStaticNew || function != functions[0])
                sb.AppendLine();
            EmitInteropMethod(sb, className, function, exportNames[function]);
        }
        sb.AppendLine("    #endregion");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static readonly HashSet<string> CSharpKeywords = new(StringComparer.Ordinal)
    {
        "abstract","as","base","bool","break","byte","case","catch","char","checked",
        "class","const","continue","decimal","default","delegate","do","double","else",
        "enum","event","explicit","extern","false","finally","fixed","float","for",
        "foreach","goto","if","implicit","in","int","interface","internal","is","lock",
        "long","namespace","new","null","object","operator","out","override","params",
        "private","protected","public","readonly","ref","return","sbyte","sealed","short",
        "sizeof","stackalloc","static","string","struct","switch","this","throw","true",
        "try","typeof","uint","ulong","unchecked","unsafe","ushort","using","virtual",
        "void","volatile","while",
    };

    private static string EscapeIdentifier(string name) => CSharpKeywords.Contains(name) ? "@" + name : name;

    private void EmitPublicMethod(StringBuilder sb, string className, WhitelistFunction function, string exportName)
    {
        var isInternalPointerReturn = IsPrimitivePointer(function.Return.Type);
        var isValueStructReturn = TypeClassifier.IsVtkValueStruct(function.Return.Type);
        var methodName = isInternalPointerReturn ? $"{function.Name}_Internal" : function.Name;
        var visibility = isInternalPointerReturn ? "internal" : "public";
        var returnType = ToPublicType(function.Return.Type);
        var parameterEntries = function.Parameters
            .Select(p => (PublicType: GetPublicParameterType(p), Name: EscapeIdentifier(p.Name)))
            .ToList();
        var parameters = string.Join(", ", parameterEntries.Select(e => $"{e.PublicType} {e.Name}"));

        string? outVarName = null;
        if (isValueStructReturn)
        {
            var componentCount = TypeClassifier.GetValueStructComponentCount(function.Return.Type);
            outVarName = $"__out{function.Name}";
        }

        sb.AppendLine($"    {visibility} new {returnType} {methodName}({parameters})");
        sb.AppendLine("    {");

        var fixedParameters = function.Parameters.Where(IsSpanParameter).ToList();
        var indent = "        ";
        foreach (var parameter in fixedParameters)
        {
            var escapedName = EscapeIdentifier(parameter.Name);
            sb.AppendLine($"{indent}fixed ({GetSpanElementType(parameter.Type)}* {parameter.Name}Ptr = {escapedName})");
            sb.AppendLine($"{indent}{{");
            indent += "    ";
        }

        if (isValueStructReturn)
        {
            var componentCount = TypeClassifier.GetValueStructComponentCount(function.Return.Type);
            sb.AppendLine($"{indent}double* {outVarName} = stackalloc double[{componentCount}];");
        }

        var hasStringParameter = function.Parameters.Any(p => IsStringPointer(p.Type));

        if (hasStringParameter)
        {
            sb.AppendLine($"{indent}#if NET10_0_OR_GREATER");
            var call10 = FormatCall(exportName, function.Parameters, p => ToInteropArgument(p), outVarName);
            EmitCall(sb, indent, function.Return.Type, call10, outVarName);
            sb.AppendLine($"{indent}#else");
            var call20 = FormatCall(exportName, function.Parameters, p =>
                IsStringPointer(p.Type) ? $"VtkString.ToNullTerminatedUtf8({EscapeIdentifier(p.Name)})" : ToInteropArgument(p), outVarName);
            EmitCall(sb, indent, function.Return.Type, call20, outVarName);
            sb.AppendLine($"{indent}#endif");
        }
        else
        {
            var call = FormatCall(exportName, function.Parameters, ToInteropArgument, outVarName);
            EmitCall(sb, indent, function.Return.Type, call, outVarName);
        }

        for (var i = fixedParameters.Count - 1; i >= 0; i--)
        {
            indent = indent[..^4];
            sb.AppendLine($"{indent}}}");
        }

        sb.AppendLine("    }");
    }

    private static string FormatCall(string exportName, IReadOnlyList<WhitelistParameter> parameters, Func<WhitelistParameter, string> argumentSelector, string? extraArg = null)
    {
        var args = new List<string> { "this.NativePointer" };
        args.AddRange(parameters.Select(argumentSelector));
        if (extraArg is not null)
            args.Add(extraArg);
        return $"{exportName}({string.Join(", ", args)})";
    }

    private static void EmitCall(StringBuilder sb, string indent, string returnType, string call, string? outVarName = null)
    {
        if (TypeClassifier.IsVtkValueStruct(returnType))
        {
            var csTypeName = TypeClassifier.GetValueStructCSharpName(returnType);
            var componentCount = TypeClassifier.GetValueStructComponentCount(returnType);
            sb.AppendLine($"{indent}{call};");
            var ctorArgs = string.Join(", ", Enumerable.Range(0, componentCount).Select(i => $"{outVarName}[{i}]"));
            sb.AppendLine($"{indent}return new {csTypeName}({ctorArgs});");
            return;
        }

        if (returnType == "void")
        {
            sb.AppendLine($"{indent}{call};");
        }
        else if (TypeClassifier.TryGetVtkClassPointerName(returnType, out var returnClassName))
        {
            sb.AppendLine($"{indent}return {returnClassName}.WeakReference({call});");
        }
        else if (IsStringPointer(returnType))
        {
            sb.AppendLine($"{indent}return VtkString.FromUtf8Pointer({call});");
        }
        else if (returnType == "char")
        {
            sb.AppendLine($"{indent}return (char){call};");
        }
        else if (returnType == "vtkTypeBool")
        {
            sb.AppendLine($"{indent}return {call} != 0;");
        }
        else
        {
            sb.AppendLine($"{indent}return {call};");
        }
    }

    private static void EmitInteropMethod(StringBuilder sb, string className, WhitelistFunction function, string exportName)
    {
        var isValueStructReturn = TypeClassifier.IsVtkValueStruct(function.Return.Type);
        var interopReturnType = isValueStructReturn ? "void" : ToInteropReturnType(function.Return.Type);
        var returnMarshal = isValueStructReturn ? null : ToReturnMarshalAttribute(function.Return.Type);
        var hasStringParameter = function.Parameters.Any(p => IsStringPointer(p.Type));

        string extraParam = "";
        if (isValueStructReturn)
        {
            extraParam = $", double* __out{function.Name}";
        }

        if (hasStringParameter)
        {
            sb.AppendLine("#if NET10_0_OR_GREATER");
            sb.AppendLine($"    [LibraryImport(InteropInfo.NativeLibraryName, StringMarshalling = StringMarshalling.Utf8)]");
            if (returnMarshal is not null)
                sb.AppendLine($"    {returnMarshal}");
            var net10Params = string.Join(", ", new[] { "nint self" }.Concat(
                function.Parameters.Select(p => IsStringPointer(p.Type)
                    ? $"string {EscapeIdentifier(p.Name)}"
                    : ToInteropParameter(p)))) + extraParam;
            sb.AppendLine($"    private static partial {interopReturnType} {exportName}({net10Params});");
            sb.AppendLine("#else");
            sb.AppendLine($"    [DllImport(InteropInfo.NativeLibraryName)]");
            if (returnMarshal is not null)
                sb.AppendLine($"    {returnMarshal}");
            var ns20Params = string.Join(", ", new[] { "nint self" }.Concat(
                function.Parameters.Select(p => IsStringPointer(p.Type)
                    ? $"byte[] {EscapeIdentifier(p.Name)}"
                    : ToInteropParameter(p)))) + extraParam;
            sb.AppendLine($"    private static extern {interopReturnType} {exportName}({ns20Params});");
            sb.AppendLine("#endif");
        }
        else
        {
            sb.AppendLine("    [DllImport(InteropInfo.NativeLibraryName)]");
            if (returnMarshal is not null)
                sb.AppendLine($"    {returnMarshal}");
            var parameters = string.Join(", ", new[] { "nint self" }.Concat(function.Parameters.Select(ToInteropParameter))) + extraParam;
            sb.AppendLine($"    private static extern {interopReturnType} {exportName}({parameters});");
        }
    }

    private static string ToPublicType(string type)
    {
        if (TypeClassifier.IsVtkValueStruct(type))
            return TypeClassifier.GetValueStructCSharpName(type);

        if (TypeClassifier.TryGetVtkClassPointerName(type, out var className))
            return className;

        return type switch
        {
            "void" => "void",
            "char" => "char",
            "int" => "int",
            "unsigned int" => "uint",
            "unsigned long" => "ulong",
            "long long" => "long",
            "unsigned long long" => "ulong",
            "double" => "double",
            "float" => "float",
            "bool" => "bool",
            "vtkTypeBool" => "bool",
            "vtkTypeUInt32" => "uint",
            "vtkIdType" => "long",
            "const char*" or "char*" => "string",
            "void*" => "nint",
            "HWND" or "HDC" or "HGLRC" => "nint",
            _ when IsPrimitivePointer(type) => $"{ToPublicType(TypeClassifier.GetPointerElementType(type))}*",
            _ when IsFixedArray(type) => ToPublicArrayType(type),
            _ => throw new NotSupportedException($"C# public type '{type}' is not supported by the MVP emitter."),
        };
    }

    private static string ToInteropType(string type)
    {
        if (TypeClassifier.IsVtkValueStruct(type))
            return "void";

        if (TypeClassifier.TryGetVtkClassPointerName(type, out _))
            return "nint";

        return type switch
        {
            "void" => "void",
            "char" => "char",
            "int" => "int",
            "unsigned int" => "uint",
            "unsigned long" => "ulong",
            "long long" => "long",
            "unsigned long long" => "ulong",
            "double" => "double",
            "float" => "float",
            "bool" => "bool",
            "vtkTypeBool" => "int",
            "vtkTypeUInt32" => "uint",
            "vtkIdType" => "long",
            "const char*" or "char*" => "nint",
            "void*" => "nint",
            "HWND" or "HDC" or "HGLRC" => "nint",
            _ when IsPrimitivePointer(type) => $"{ToInteropType(TypeClassifier.GetPointerElementType(type))}*",
            _ when IsFixedArray(type) => $"{GetArrayElementType(type)}*",
            _ => throw new NotSupportedException($"C# interop type '{type}' is not supported by the MVP emitter."),
        };
    }

    private static string ToInteropReturnType(string type)
        => type == "char" ? "byte" : ToInteropType(type);

    private static string ToInteropArgument(WhitelistParameter parameter)
    {
        var name = EscapeIdentifier(parameter.Name);
        if (TypeClassifier.TryGetVtkClassPointerName(parameter.Type, out _))
            return $"{name}.NativePointer";

        if (IsSpanParameter(parameter))
            return $"{parameter.Name}Ptr";

        if (parameter.Type == "char")
            return $"(byte){name}";

        if (parameter.Type == "vtkTypeBool")
            return $"{name} ? 1 : 0";

        return name;
    }

    private static string ToInteropParameter(WhitelistParameter parameter)
    {
        var marshal = ToParameterMarshalAttribute(parameter.Type);
        var prefix = marshal is null ? "" : $"{marshal} ";
        return $"{prefix}{ToInteropParameterType(parameter.Type)} {EscapeIdentifier(parameter.Name)}";
    }

    private static string ToInteropParameterType(string type)
        => IsStringPointer(type) ? "string" :
           type == "char" ? "byte" :
           ToInteropType(type);

    private Dictionary<WhitelistFunction, string> CreateExportNames(string className, IReadOnlyList<WhitelistFunction> functions)
    {
        return this._exportNameGenerator.CreateAll(className,
            functions.Select(f => (f, f.Name, (IReadOnlyList<CanonicalType>)f.Parameters.Select(p => new CanonicalType(p.Type)).ToList())).ToList());
    }

    private static string? ToReturnMarshalAttribute(string type)
        => type switch
        {
            "bool" => "[return: MarshalAs(UnmanagedType.U1)]",
            _ => null,
        };

    private static string? ToParameterMarshalAttribute(string type)
        => type switch
        {
            "bool" => "[MarshalAs(UnmanagedType.U1)]",
            _ => null,
        };

    private static bool IsStringPointer(string type)
        => type is "const char*" or "char*";

    private static bool IsPrimitivePointer(string type)
        => TypeClassifier.IsSupportedPrimitivePointerType(type);

    private static bool IsFixedArray(string type)
        => type.EndsWith(']') && type.Contains('[', StringComparison.Ordinal);

    private static string ToPublicArrayType(string type)
    {
        var elementType = GetArrayElementType(type);
        return type.StartsWith("const ", StringComparison.Ordinal)
            ? $"ReadOnlySpan<{elementType}>"
            : $"Span<{elementType}>";
    }

    private static string GetArrayElementType(string type)
    {
        var bracketIndex = type.IndexOf('[', StringComparison.Ordinal);
        var elementType = type[..bracketIndex].Replace("const ", "", StringComparison.Ordinal).Trim();
        return elementType;
    }

    private static bool IsSpanParameter(WhitelistParameter parameter)
        => IsFixedArray(parameter.Type) ||
           (IsPrimitivePointer(parameter.Type) && HasPointerMetadata(parameter));

    private static bool HasPointerMetadata(WhitelistParameter parameter)
        => parameter.Direction is not null && parameter.Length is not null;

    private static string GetPublicParameterType(WhitelistParameter parameter)
    {
        if (IsSpanParameter(parameter))
            return GetSpanPublicType(parameter);
        return ToPublicType(parameter.Type);
    }

    private static string GetSpanPublicType(WhitelistParameter parameter)
    {
        var elementType = GetSpanElementType(parameter.Type);
        var isReadOnly = IsFixedArray(parameter.Type)
            ? parameter.Type.StartsWith("const ", StringComparison.Ordinal)
            : parameter.Direction == "in";
        return isReadOnly ? $"ReadOnlySpan<{elementType}>" : $"Span<{elementType}>";
    }

    private static string GetSpanElementType(string type)
    {
        if (IsFixedArray(type))
            return GetArrayElementType(type);
        if (IsPrimitivePointer(type))
            return ToPublicType(TypeClassifier.GetPointerElementType(type));
        throw new ArgumentException($"Cannot get span element type from '{type}'.", nameof(type));
    }
}
